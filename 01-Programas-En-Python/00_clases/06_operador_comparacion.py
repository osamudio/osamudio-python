# Ejemplo del operador igual que (==)
primero = 5
segundo = 6
igual_que = primero == segundo
print(f"¿El {primero} y {segundo} son iguales? : {igual_que}")

# Ejemplo del operador distinto de (!=)
distinto_de = primero != segundo
print(f"¿El {primero} y {segundo} son diferentes? : {distinto_de}")

# Ejemplo del operador menor que (<)
menor_que = primero < segundo
print(f"¿El {primero} es menor que {segundo} ? : {menor_que}")

# Ejemplo del operador mayor que (>)
mayor_que = primero > segundo
print(f"¿El {primero} es mayor que {segundo} ? : {mayor_que}")

# Ejemplo del operador menor o igual (<=)
menor_o_igual = primero <= segundo
print(f"¿El {primero} es <= que {segundo} ? : {menor_o_igual}")

# Ejemplo del operador mayor o igual (>=)
mayor_o_igual = primero >= segundo
print(f"¿El {primero} es >= que {segundo} ? : {mayor_o_igual}")

# Ejemplo de comparación con operadores
primero = 5
segundo = 6
tercero = 11
resultado = primero + segundo == tercero
print(f"{primero} + {segundo} es igual {tercero} : {resultado}")

# Eliminación de variables
del primero
